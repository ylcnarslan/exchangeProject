DROP TABLE IF EXISTS EXCHANGE_RATE;
 
CREATE TABLE EXCHANGE_RATE (
  id INT AUTO_INCREMENT  PRIMARY KEY,
  source VARCHAR(250) DEFAULT NULL,
  rate VARCHAR(250) DEFAULT NULL,
  value VARCHAR(250) DEFAULT NULL
);

